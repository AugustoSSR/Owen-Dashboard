        <section class="section">
            <h3>DADOS DA INSTALAÇÃO:</h3>
            <p>O material a ser empregado será cabo de fibra óptica do tipo CFOA-SM-AS-80-12-FO constituído por fibras monomodo posicionadas em tubos. Os tubos são reunidos em torno de um elemento central dielétrico e protegidos por uma capa interna armação de fios de aramida e capa externa de polietileno que pode ser de material retardante à chama. São indicados para instalação aérea autossustentados em vãos máximos de 80 metros.</p>
            <p>A metragem da fibra óptica a ser instalada é de 23.383 metros.</p>
            <p>O Peso da fibra óptica CFOA-SM-AS-80-12-FO a ser instalada é de 54 kg/km.</p>
            <p>Não será necessária nenhuma troca de postes.</p>
        </section>

        <section class="section">
            <h3>CARACTERÍSTICAS TÉCNICAS:</h3>
            <p>Foram definidos em planta a intensidade direção e sentido do esforço resultante a 02 metros do topo do poste os esforços das redes da CEEE do cabo óptico da empresa UNIFIQUE TELECOMUNICAÇÕES S/A e dos outros ocupantes instalados nos postes;</p>
            <p>O ocupante obedecerá ao afastamento mínimo do neutro de 15 metros (se ocupar a primeira faixa de utilização de uso mútuo) respeitando as normas dos órgãos competentes no que se refere à luz mínima em relação ao solo na condição mais desfavorável.</p>
            <p>O cálculo das trações de montagem do conjunto do Sistema Óptico foi elaborado considerando-se um vão médio entre postes de 35 metros.</p>
